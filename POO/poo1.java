/*Herança é quando uma classe recebe características e comportamentos de outra, permitindo reutilizar código e criar hierarquias.
Por exemplo, um carro elétrico é um tipo de carro, então se você tiver uma classe Carro com atributos como marca e modelo e métodos como acelerar,
a classe CarroEletrico pode herdar tudo isso e ainda adicionar algo específico, como carregarBateria.

Polimorfismo é a capacidade de um objeto se comportar de maneiras diferentes dependendo do contexto. Por exemplo, um funcionário pode ter diferentes cargos,
como Gerente ou Vendedor, mas todos têm um método calcularSalario. Para o gerente, esse método inclui bônus, enquanto para o vendedor inclui comissão.

Abstração é o processo de esconder detalhes complexos e mostrar apenas o essencial para o uso do objeto. Por exemplo, um smartphone tem muitos circuitos internos, mas o usuário
só precisa saber que pode fazer ligações, enviar mensagens ou abrir aplicativos. Em Java, você pode criar uma classe abstrata Dispositivo com métodos
como ligar ou desligar sem se preocupar com os detalhes internos de como esses métodos funcionam em cada dispositivo específico.

Encapsulamento é quando protegemos os dados de uma classe para que só possam ser acessados ou modificados de maneiras controladas, geralmente usando métodos getters e setters. 
Por exemplo, em um banco, a conta de um cliente tem um saldo. Ninguém deveria alterar o saldo diretamente, apenas através de métodos como depositar ou sacar,
garantindo que não ocorram inconsistências. */